def analyse_shellcode(name: str, shellcode: bytes) -> None:
    print("=" * 60)
    print(f"Analyse du shellcode : {name}")
    print(f"Taille               : {len(shellcode)} octets")
    print(f"Représentation hex   : {shellcode.hex()}")
    print("-" * 60)

    emulator = pylibemu.Emulator()

    offset = emulator.shellcode_getpc_test(shellcode)

    if offset >= 0:
        print(f"Shellcode détecté ! Offset GetPC : {offset}")
    else:
        print("Aucun shellcode détecté par GetPC test.")

    emulator.run(shellcode)

    profile = emulator.emu_profile_output

    if profile:
        print("Profil d'émulation :")
        print(profile)
    else:
        print("Aucun profil généré.")

    print("Chaînes ASCII détectées dans le shellcode :")
    found = extract_strings(shellcode)
    if found:
        for s in found:
            print(f"    → {s}")
    else:
        print("Aucune chaîne trouvée.")

    print("=" * 60)


def extract_strings(data: bytes, min_length: int = 4) -> list:
    result = []
    current = ""
    for byte in data:
        if 32 <= byte <= 126:
            current += chr(byte)
        else:
            if len(current) >= min_length:
                result.append(current)
            current = ""
    if len(current) >= min_length:
        result.append(current)
    return result


